async def seed():
    logging.basicConfig(level=logging.INFO)
    print("⏳ Подключаемся к Supabase для заливки меню...")

    # Здесь тоже глушим кэш стейтментов для PgBouncer
    engine = create_async_engine(
        settings.db_url,
        connect_args={"statement_cache_size": 0}
    )

    # Автоматически докидываем колонку description в базу, если её там нет
    async with engine.begin() as conn:
        print("🔧 Проверяем и обновляем структуру таблицы в облаке...")
        await conn.execute(text("ALTER TABLE products ADD COLUMN IF NOT EXISTS description VARCHAR(500);"))

    async_session = async_sessionmaker(engine, expire_on_commit=False)

    async with async_session() as session:
        for category_name, products in RAW_MENU.items():
            print(f"📦 Обработка категории: {category_name}")
            for name, price, desc in products:
                # Безопасно проверяем через ORM, существует ли уже такая позиция
                stmt = select(Product.id).where(Product.category == category_name, Product.name == name)
                check_exist = (await session.execute(stmt)).scalar()

                if not check_exist:
                    new_product = Product(
                        category=category_name,
                        name=name,
                        price=price,
                        description=desc,
                        photo_id="",
                        is_active=True
                    )
                    session.add(new_product)

        await session.commit()

    print("✨ Все охуенно! Меню со всеми описаниями и иконками успешно залито в Supabase.")
    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())